// тут проверяем дату на адекватность
bool isValid(int y, int m, int d) {
    // если год вне диапазона, месяцев не 12 или день меньше 1 — сразу мимо
    if (y < 1900 || y > 2100 || m < 1 || m > 12 || d < 1) return false;
    
    // список дней по месяцам. ноль в начале, чтобы номер месяца совпадал с индексом
    int days[13] = { 0, 31, 28, 31, 30, 31, 30, 31, 31, 30, 31, 30, 31 };
    
    // магия високосного года: если февраль и год делится на 4 (но не на 100) или на 400...
    if (m == 2 && ((y % 4 == 0 && y % 100 != 0) || y % 400 == 0)) {
        days[2] = 29; // ...то в феврале становится 29 дней
    }
    // проверяем, что в забитом месяце не превышен лимит дней
    return d <= days[m];
}

// превращаем кашу из цифр в красивую строчку времени
std::string formatTimeStr(int hms) {
    int hh = hms / 10000;               // часы 
    int mm = (hms % 10000) / 100;       // минуты 
    int ss = hms % 100;                 // секунды 
    
    std::string res = "";
    
    // клеим строку и добавляем нули, если число меньше 10 (чтобы было "05", а не "5")
    if (hh < 10) res += "0"; res += std::to_string(hh) + ":";
    if (mm < 10) res += "0"; res += std::to_string(mm) + ":";
    if (ss < 10) res += "0"; res += std::to_string(ss);
    
    return res;
}